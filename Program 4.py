#Program 4: Using Math Operators
#Addition
print(2+3)
print(2.0+30)

#Subtraction
print(2.0-3.0)
print(2-3)

#Multiplication
print(2*3)

#Division
print(2/3)

#Integer Division
print(2.0 // 3)
print(-6.0 // -3)

#Exponentiation 
print(2.0**3)

#Exponentiation Using Floating Point Numbers 
print(2**3.0)

#Modulus
print(2%3)